[CreateAssetMenu(fileName = "Pokemon", menuName = "Pokemon/Create new pokemon")]

public class PokemonBase : ScriptableObject
{
    [SerializeField] string name;

    [TextArea]
    [SerializeField] string description;

    // Sprites
    [SerializeField] Sprite frontSprite;
    [SerializeField] Sprite backSprite;

    // Types
    [SerializeField] PokemonType type1;
    [SerializeField] PokemonType type2;

    // Base Stats
    [SerializeField] int maxHp;
    [SerializeField] int attack;
    [SerializeField] int defense;
    [SerializeField] int spAttack;
    [SerializeField] int spDefense;
    [SerializeField] int speed;

    [SerializeField] int expYield;
    [SerializeField] GrowthRate growthRate;

    [SerializeField] int catchRate = 255;

    // LearnableMoves
    [SerializeField] List<LearnableMove> learnableMoves;

    public static int MaxNumOfMoves { get; set; } = 4;

    /// <summary>
    /// Returns experience for each level per https://bulbapedia.bulbagarden.net/wiki/Experience#Relation_to_level
    /// </summary>
    /// <param name="level"></param>
    /// <returns>Experience as integer</returns>
    public int GetExpForLevel(int level)
    {
        switch (growthRate)
        {
            case GrowthRate.Fast:
                return 4 * (level * level * level) / 5;
            case GrowthRate.MediumFast:
                return level * level * level;
            case GrowthRate.MediumSlow:
                return (6 * (level * level * level) / 5) - (15 * (level * level)) + (100 * level) - 140;
            case GrowthRate.Slow:
                return 5 * (level * level * level) / 4;
            case GrowthRate.Fluctuating:
                if (level < 15)
                {
                    return (level * level * level) * (Mathf.FloorToInt((level + 1) / 3) + 24) / 50;
                }
                else if (level < 36)
                {
                    return (level * level * level) * (level + 14) / 50;
                }
                else
                {
                    return (level * level * level) * (Mathf.FloorToInt(level / 2) + 32) / 50;
                }
            case GrowthRate.Erratic:
                if (level < 50)
                {
                    return (level * level * level) * (100 - level) / 50;
                }
                else if (level < 68)
                {
                    return (level * level * level) * (150 - level) / 100;
                }
                else if (level < 98)
                {
                    return (level * level * level) * Mathf.FloorToInt((1911 - 10 * level) / 3) / 500;
                }
                else
                {
                    return (level * level * level) * (160 - level) / 100;
                }
        }

        return -1; // If error...
    }

    public string Name
    {
        get { return name; }
    }

    public string Description
    {
        get { return description; }
    }

    public Sprite FrontSprite
    {
        get { return frontSprite; }
    }

    public Sprite BackSprite
    {
        get { return backSprite; }
    }

    public PokemonType Type1
    {
        get { return type1; }
    }

    public PokemonType Type2
    {
        get { return type2; }
    }

    public int MaxHp
    {
        get { return maxHp; }
    }

    public int Attack
    {
        get { return attack; }
    }

    public int SpAttack
    {
        get { return spAttack; }
    }

    public int Defense
    {
        get { return defense; }
    }

    public int SpDefense
    {
        get { return spDefense; }
    }

    public int Speed
    {
        get { return speed; }
    }

    public List<LearnableMove> LearnableMoves
    {
        get { return learnableMoves; }
    }

    public int ExpYield => expYield;

    public GrowthRate GrowthRate => growthRate;

    public int CatchRate => catchRate;
}

[System.Serializable]
public class LearnableMove
{
    [SerializeField] MoveBase moveBase;
    [SerializeField] int level;

    public MoveBase Base
    {
        get { return moveBase; }
    }

    public int Level
    {
        get { return level; }
    }
}
public enum PokemonType
{
    None,
    Normal,
    Fire,
    Water,
    Electric,
    Grass,
    Ice,
    Fighting,
    Poison,
    Ground,
    Flying,
    Psychic,
    Bug,
    Rock,
    Ghost,
    Dragon,
    Dark,
    Steel,
    Fairy
}

public enum GrowthRate
{
    Fast,
    MediumFast,
    MediumSlow,
    Slow,
    Fluctuating,
    Erratic
}

public enum Stat
{
    Attack,
    Defense,
    SpAttack,
    SpDefense,
    Speed,

    // These 2 are not actual stats, they're used to boost the moveAccuracy
    Accuracy,
    Evasion,
}

